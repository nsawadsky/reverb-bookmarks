public class HistoryMinerPluginException extends Exception {

    public HistoryMinerPluginException() {
        super();
    }

    public HistoryMinerPluginException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public HistoryMinerPluginException(String arg0) {
        super(arg0);
    }

    public HistoryMinerPluginException(Throwable arg0) {
        super(arg0);
    }

}
